#include <stdio.h>

int main(int argc, char const *argv[])
{
    
    float f = 3.14;
    printf("sizeof(float):%ld\n"  , sizeof(float));
    printf("sizeof(double):%ld\n"  , sizeof(double));
    printf("sizeof(long double):%ld\n"  , sizeof(long double));

    printf("f:%f\n" , f );
    printf("d:%d\n" , f );






    return 0;
}
